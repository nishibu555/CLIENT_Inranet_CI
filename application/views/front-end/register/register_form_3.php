    $("#register_form_3").validate({
        submitHandler: function () {

            var third_form_value = $('#register_form_3').serialize();

            $.ajax({
                    method:'POST',
                    data:third_form_value,
                    url:'save_registration_form3',
                    success:function (data) {
                        console.log(data);
                        var res = JSON.parse(data);
                        if (res.error_list)
                        {
                            $.each(res.error_list, function(key,val) {
                                $('.'+key).text(val).css('color','red');
                            });
                        }
                        else
                        {
                            $("#p3").removeClass('active');
                            $("#p4").addClass('active');
                            changeNavStatus(3);
                            
                             $.ajax({ url: 'register_form_4', 
                                success: function(html) {
                                     $("#ajax-content").empty().append(html);
                                }
                             });
                        }
                    }
            });
        }
    });

</script>

   
